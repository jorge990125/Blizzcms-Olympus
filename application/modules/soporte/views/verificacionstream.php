        <h2 style="text-align: center;">Verificación para Stream en Soa-WoW</h2>
        
        <h3>Requisitos para Streamers en Soa-WoW</h3>
        <p>Si eres streamer y deseas transmitir sobre nuestro servidor, debes cumplir con los siguientes requisitos para poder disfrutar de los beneficios exclusivos que ofrecemos:</p>
        
        <ul>
            <li><strong>Horas de streaming:</strong> Debes cumplir con un mínimo de 20 horas de streaming mensuales en plataformas como Twitch, YouTube, etc.</li>
            <li><strong>Seguidores:</strong> Necesitas tener al menos 100 seguidores en tu plataforma de streaming.</li>
            <li><strong>Vistas por transmisión:</strong> Tu transmisión debe tener un mínimo de 50 vistas por sesión.</li>
            <li><strong>Cumplir con las reglas del servidor:</strong> Debes seguir todas las reglas establecidas por el servidor y ser un modelo de comportamiento para la comunidad.</li>
        </ul>

        <h4>Beneficios para Streamers:</h4>
        <p>Si cumples con los requisitos anteriores, recibirás los siguientes beneficios exclusivos:</p>
        <ul>
            <li><strong>Acceso a monturas y objetos exclusivos:</strong> Recibirás premios especiales por tu colaboración.</li>
            <li><strong>Soporte prioritario:</strong> Tendrás acceso a soporte técnico inmediato si necesitas ayuda durante tus transmisiones.</li>
            <li><strong>Título especial:</strong> Obtendrás un título especial como "Streamer de Soa-WoW".</li>
            <li><strong>Promoción:</strong> Tu canal será promocionado en las redes sociales del servidor.</li>
        </ul>		
		
		<h4>Uso de anuncios personalizados de directos :</h4>
        <img src="/application/themes/default/assets/images/stream/stream.png" alt="Imagen de fin" title="Fin del documento">
        <p>El streamer tendra acceso a ciertos comandos necesarios para relaizar la suscripción del canal o canles para sus anuncios personalizados (cualquier jugador tiene acceso a los comandos pero solo los streamer verificados podran realizar los anuncios):</p>
        <ul>
            <li><strong>Registro del canal:</strong> /stream add <plataforma> <canal> (ejemplo para youtube: /stream add youtube SOA_TV). Plataformas disponibles (kick, youtube, facebook, instagram, tiktok.</li>
            <li><strong>Registro del nombre de Streamer:</strong> /stream setname <nombre> (ejemplo /stream setname SOA).</li>
            <li><strong>Enviar anuncio del directo:</strong> /stream anuncio <plataforma> (ejemplo /stream anuncio youtube - solo los streamer verificados podran usarlo). Plataformas disponibles (kick, youtube, facebook, instagram, tiktok.</li>
        </ul>
		
